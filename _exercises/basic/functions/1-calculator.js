/*

    Create the calculator functions, add, substract, multiply, and divide.

    Make sure you can't divide by zero.

 */


function add (x, y) {
    return x + y;
}


function substract (x, y) {
    return x - y;
}


function multiply (x, y) {
    return x * y;
}


function divide (x, y) {
    return x / y;
}

console.log ( "Added=" + add ( 7,4) );

console.log ( "substracted=" + substract ( 7,4) );

console.log ( "multiplied=" + multiply( 7,4) );

console.log ( "divided=" + divide( 7,4) );

